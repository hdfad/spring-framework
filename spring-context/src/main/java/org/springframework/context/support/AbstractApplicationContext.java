	/**
	 * 覆写了ConfigurableApplicationContext接口中的方法
	 * 用于刷新spring上下文信息
	 * 整个spring的生命周期、aop、ioc阶段均由此流程开始解决,13个主要方法,5个扩展点
	 * ioc阶段：创建bean工厂->填充bean工厂属性->完成bean工厂的赋值和开始加载非懒加载的bean
	 *		1.在创建bean工厂之前会spring的状态、属性、监听器等进行初始化和校验，然后会通过obtainFreshBeanFactory构建一个新的DefaultListableBeanFactory工厂，
	 *			如果之前存在则销毁，通过BeanDefinitionReader读取xml或者注解等配置信息，并装载在BeanDefinition容器中
	 *		2.对bean工厂属性进行填充，设置类加载器,aop支持等
	 *		3.通过finishBeanFactoryInitialization方法完成bean的赋值并使用当前beanFactory开始对非懒加载的bean进行初始化
	 *			3.1.通过preInstantiateSingletons
	 *
	 * @throws BeansException
	 * @throws IllegalStateException
	 */
	@Override
	public void refresh() throws BeansException, IllegalStateException {
		//使用synchronized代码块，在并发下refresh时是安全的
		synchronized (this.startupShutdownMonitor) {
			StartupStep contextRefresh = this.applicationStartup.start("spring.context.refresh");

			// Prepare this context for refreshing.
			/**
			 * 预刷新容器
			 * 1：设置容器启动时间
			 * 2：设置活跃状态为true
			 * 3：设置关闭状态为false
			 * 4：构建环境Environment缓存并校验
			 * 5：准备监听器和事件的集合对象，默认为空集合
			 */
			prepareRefresh();

			// Tell the subclass to refresh the internal bean factory.
			/**
			 * 创建bean工厂、设置循环依赖状态
			 * 针对于不同的ApplicationContext调用不同的实现类
			 * 注解上下文指会创建往bean工厂中设置一个id，
			 * xml上下文除id外，还要处理对xml标签lookup-method、replaced-method bean的支持，需要计算xml文件数量
			 */
			ConfigurableListableBeanFactory beanFactory = obtainFreshBeanFactory();

			// Prepare the bean factory for use in this context.
			//应用bean工厂属性，包括类加载器，增加AspectJ的支持、
			//依赖查找，内建bean对象、依赖、非bean对象
			/**
			 *	预初始化bean工厂
			 *	1.设置类加载器
			 *  2.添加对spel表达式解析器
			 *  3.添加属性编辑器
			 *  4.添加ApplicationContextAwareProcessor处理器
			 *  5.忽略依赖注入部分Aware接口
			 *  6.注册部分依赖
			 *  7.提供对LTW的支持
			 *  8.注册系统环境
			 */
			prepareBeanFactory(beanFactory);

			try {
				// Allows post-processing of the bean factory in context subclasses.
				//空方法，模板方法，自定义进行实现扩充对一些beanFactory的设置、注册、忽略
				postProcessBeanFactory(beanFactory);

				//start->end
				StartupStep beanPostProcess = this.applicationStartup.start("spring.context.beans.post-process");
				// Invoke factory processors registered as beans in the context.

				/**
				 * 初始化前一个扩展点，通过BeanFactoryPostProcessors对BeanDefinition进行扩展，填充BeanFactory
				 * 调用bean工厂后置处理器,加载并注册BeanDefinition信息
				 * 1:加载注解类
				 * @see org.springframework.context.annotation.ConfigurationClassParser#doProcessConfigurationClass(org.springframework.context.annotation.ConfigurationClass, org.springframework.context.annotation.ConfigurationClassParser.SourceClass, java.util.function.Predicate)
				 * 	1.1:@PropertySource,加载*.properties文件
				 * 		@see org.springframework.context.annotation.ConfigurationClassParser#processPropertySource(org.springframework.core.annotation.AnnotationAttributes)
				 * 	1.2:@ComponentScan,加载配置扫描路劲
				 * 		@see org.springframework.context.annotation.ComponentScanAnnotationParser#parse(org.springframework.core.annotation.AnnotationAttributes, java.lang.String)
				 * 	1.3:@Import,注解类解析到容器中
				 * 		@see org.springframework.context.annotation.ConfigurationClassParser.ImportStack#registerImport(org.springframework.core.type.AnnotationMetadata, java.lang.String)
				 * 	1.4:@ImportResource,引入spring-xml配置到容器中
				 *
				 * 	1.5:@Bean对象注入
				 */
				invokeBeanFactoryPostProcessors(beanFactory);

				// Register bean processors that intercept bean creation.
				//注册bean后置处理器,将BeanPostProcessors根据实现的接口是PriorityOrdered、Ordered还是其他的按顺序加载到容器中，首先注册实现PriorityOrdered的类，再注册实现了Ordered的类，最后再注册其他的类

				/**
				 * 注册后置处理器
				 * 对bean后置处理器组件进行注册，后续在后createBean时需要调用多个后置处理器完成对容器属性的注入和依赖，如@PostConstruct注册、@Resource注册等
				 */
				registerBeanPostProcessors(beanFactory);
				beanPostProcess.end();

				// Initialize message source for this context.
				// 消息源初始化-处理国际化
				initMessageSource();

				// Initialize event multicaster for this context.
				/**
				 * 初始化事件多播器：applicationEventMulticaster
				 * 构建事件监听多路广播器ApplicationEventMulticaster(SimpleApplicationEventMulticaster)
				 * 事件的执行主要是在Bean初始化之后通过上下文调用publishEvent方法执行
				 */
				initApplicationEventMulticaster();

				// Initialize other special beans in specific context subclasses.
				//容器刷新时执行的自定义逻辑，交给子类，空方法
				onRefresh();

				// Check for listener beans and register them.
				/**
				 * 向多播器applicationEventMulticaster中注册监听器
				 * 获取所有的监听类beanName添加到事件多播器ApplicationEventMulticaster中
				 */
				registerListeners();

				// Instantiate all remaining (non-lazy-init) singletons.
				/**
				 * 加载非懒加载的bean，完成对bean的初始化、属性填充、实例化，过程中包含对bean增强的后置处理器、aop代理、循环依赖、注解驱动等
				 */
				finishBeanFactoryInitialization(beanFactory);

				// Last step: publish corresponding event.
				//发布事件与清除上下文环境
				/**
				 * 事件发布
				 */
				finishRefresh();
			}

			catch (BeansException ex) {
				if (logger.isWarnEnabled()) {
					logger.warn("Exception encountered during context initialization - " +
							"cancelling refresh attempt: " + ex);
				}

				// Destroy already created singletons to avoid dangling resources.
				destroyBeans();

				// Reset 'active' flag.
				cancelRefresh(ex);

				// Propagate exception to caller.
				throw ex;
			}

			finally {
				// Reset common introspection caches in Spring's core, since we
				// might not ever need metadata for singleton beans anymore...
				resetCommonCaches();
				contextRefresh.end();
			}
		}
	}

	/**
	 * Prepare this context for refreshing, setting its startup date and
	 * active flag as well as performing any initialization of property sources.
	 */
	protected void prepareRefresh() {
		// Switch to active.
		//启动时间
		this.startupDate = System.currentTimeMillis();
		//标记状态为未关闭
		this.closed.set(false);

		//处于激活状态
		this.active.set(true);

		if (logger.isDebugEnabled()) {
			if (logger.isTraceEnabled()) {
				logger.trace("Refreshing " + this);
			}
			else {
				logger.debug("Refreshing " + getDisplayName());
			}
		}

		// Initialize any placeholder property sources in the context environment.
		// 初始化一些属性设置，抽象类，子类在做具体处理
		initPropertySources();

		// Validate that all properties marked as required are resolvable:
		// see ConfigurablePropertyResolver#setRequiredProperties
		//构建Environment缓存并校验
		getEnvironment().validateRequiredProperties();


		// Store pre-refresh ApplicationListeners...
		/**
		 * 初始化earlyApplicationListeners和applicationListeners
		 */
		if (this.earlyApplicationListeners == null) {
			this.earlyApplicationListeners = new LinkedHashSet<>(this.applicationListeners);
		}
		else {
			// Reset local application listeners to pre-refresh state.
			this.applicationListeners.clear();
			this.applicationListeners.addAll(this.earlyApplicationListeners);
		}

		// Allow for the collection of early ApplicationEvents,
		// to be published once the multicaster is available...
		/**
		 * 初始化，早期监听器
		 */
		this.earlyApplicationEvents = new LinkedHashSet<>();
	}

